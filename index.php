	function historyCookie(newcookie)
	{
		if(getCookie("hist1") == newcookie)
			setCookie("hist1",newcookie,90);
		else if(getCookie("hist2") == newcookie)
		{
			setCookie("hist2",getCookie("hist1"),90);
			setCookie("hist1",newcookie,90);
		}
		else
		{
			setCookie("hist3",getCookie("hist2"),90);
			setCookie("hist2",getCookie("hist1"),90);
			setCookie("hist1",newcookie,90);			
		}
	}
	function setCookie(cname, cvalue, exdays) 
	{
    var d = new Date();
    d.setTime(d.getTime() + (exdays*24*60*60*1000));
    var expires = "expires="+d.toUTCString();
    document.cookie = cname + "=" + cvalue + "; " + expires;
	}

	function getCookie(cname) 
	{
    var name = cname + "=";
    var ca = document.cookie.split(';');
    for(var i = 0; i < ca.length; i++) {
        var c = ca[i];
        while (c.charAt(0) == ' ') {
            c = c.substring(1);
        }
        if (c.indexOf(name) == 0) {
            return c.substring(name.length, c.length);
        }
    }
    return "";
	}

